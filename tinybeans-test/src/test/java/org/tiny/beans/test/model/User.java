package org.tiny.beans.test.model;

import org.tiny.beans.sdk.annotation.Bean;

@Bean("user")
public class User {

    private String name;

    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
